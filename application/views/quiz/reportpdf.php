The Career Battery is built on the premise that core interests and work values don’t change over time, rather they define our career choices and success. It was developed with the thought of sharing insights with the student population around their interests, motivators/work drivers and their preferred thinking style(s) based on brain lateralization research. It goes beyond aptitude and ability testing. It helps students create an development plan towards a career with our on-call support, guidance and career coaching.

This report attempts to assess the following:
•	The respondent’s areas of interest
•	The respondent’s work values
•	The respondent’s thinking style
			

Dear Respondent, 
This report seeks to explore your areas of interest, work values and skills. Based on the analysis of these and an assessment of your mental preferences and thinking styles; the report suggests corresponding career proficiencies where you have a high potential of succeeding.